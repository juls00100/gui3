    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
     int row = table_evals.getSelectedRow();
    
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a record to delete.");
        return;
    }

    String evalID = table_evals.getValueAt(row, 0).toString();
    int confirm = javax.swing.JOptionPane.showConfirmDialog(null, 
            "Are you sure you want to delete this record?", "Warning", javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        Connection conn = null;
        PreparedStatement pst = null;

        try {
            // 1. Establish connection
            conn = DriverManager.getConnection("jdbc:sqlite:aes.db");
            String sql = "DELETE FROM tbl_evaluation WHERE e_id = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, evalID);

            int result = pst.executeUpdate();

            config conf = new config();
            conf.logEvent("Deleted an evaluation.");
            if (result > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
                displayEvaluations(); // Refresh the table
            }

        } catch (SQLException e) {
            // CATCH: Catching database errors
            javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        } finally {
            // FINALLY: This block ALWAYS runs, ensuring the lock is released
            try {
                if (pst != null) pst.close();
                if (conn != null) conn.close();
                System.out.println("Connection closed safely.");
            } catch (SQLException ex) {
                System.out.println("Final close error: " + ex.getMessage());
            }
        }
    }
    }//GEN-LAST:event_deleteMouseClicked

