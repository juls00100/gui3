   public void displayEvaluations() {
    String sql = "SELECT u2.u_name AS 'TEACHER NAME', "
               + "COUNT(e.e_id) AS 'TOTAL EVALUATIONS', "
               + "ROUND(AVG(e.e_average), 2) AS 'OVERALL AVERAGE' "
               + "FROM tbl_evaluation e "
               + "JOIN tbl_user u2 ON e.t_u_id = u2.u_id "
               + "GROUP BY u2.u_name "
               + "ORDER BY AVG(e.e_average) DESC"; 

    try (Connection conn = DriverManager.getConnection("jdbc:sqlite:aes.db");
         PreparedStatement pst = conn.prepareStatement(sql)) {

        try (ResultSet rs = pst.executeQuery()) {
            table_evals.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rs));
        }
    } catch (SQLException e) {
        System.out.println("Error loading evaluations: " + e.getMessage());
    }
}
    public void displayEvals() {
   String sql = "SELECT u2.u_name AS 'TEACHER NAME', "
           + "COUNT(e.e_id) AS 'TOTAL EVALUATIONS', "
           + "ROUND(AVG(e.e_average), 2) AS 'OVERALL AVERAGE' "
           + "FROM tbl_evaluation e "
           + "JOIN tbl_user u2 ON e.t_u_id = u2.u_id "
           + "GROUP BY u2.u_name " 
           + "ORDER BY AVG(e.e_average) DESC"; 
    

    try (Connection conn = DriverManager.getConnection("jdbc:sqlite:aes.db");
         PreparedStatement pst = conn.prepareStatement(sql)) {
        
        pst.setString(1, config.getID()); // Shows only evaluations for THIS teacher
        
        try (ResultSet rs = pst.executeQuery()) {
            table_evals.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rs));
        }
    } catch (SQLException e) {
        System.out.println("Error: " + e.getMessage());
    }
}

