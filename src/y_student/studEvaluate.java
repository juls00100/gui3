    private void NEXTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_NEXTMouseClicked
        
        if (isSubmitting) return;
        int currentScore = 0;
    if (r1.isSelected()) currentScore = 1;
    else if (r2.isSelected()) currentScore = 2;
    else if (r3.isSelected()) currentScore = 3;
    else if (r4.isSelected()) currentScore = 4;
    else if (r5.isSelected()) currentScore = 5;

    if (currentScore == 0 && !NEXT.getText().equals("SAVE")) {
        JOptionPane.showMessageDialog(null, "Please select a score before proceeding!");
        return;
    }
    
    if (!NEXT.getText().equals("SAVE")) {
        scores.add(currentScore);
    }
    
    scores.add(currentScore);
        if (questionsList.isEmpty()) return; 
        if (currentQuestionIndex < questionsList.size() - 1) {
        currentQuestionIndex++;
        lblQuestion.setText(questionsList.get(currentQuestionIndex));
        bg.clearSelection();
        // Change text to SAVE if we reached the last question
        if (currentQuestionIndex == questionsList.size() - 1) {
            NEXT.setText("SAVE");
            r1.setVisible(false); r2.setVisible(false); r3.setVisible(false);
            r4.setVisible(false); r5.setVisible(false);
            lblQuestion.setText("Additional Remarks/Comments:");
            
            jScrollPane1.setVisible(true);
            
            jPanel7.revalidate();
            jPanel7.repaint();
        }
        
    } else if (NEXT.getText().equals("SAVE")) {
       
        handleFinalSubmission(); 
    }
    }//GEN-LAST:event_NEXTMouseClicked

    private void PREVIOUSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PREVIOUSMouseClicked
        if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        lblQuestion.setText(questionsList.get(currentQuestionIndex));
        
        // Always set back to NEXT if not on the last question
        NEXT.setText("NEXT");
        jScrollPane1.setVisible(false);
        
        jPanel7.revalidate();
        jPanel7.repaint();
    }
    }//GEN-LAST:event_PREVIOUSMouseClicked

