    private void handleFinalSubmission() {
    if (isSubmitted) return; 

    String selectedTeacher = teacherDropdown.getSelectedItem().toString();
    String studentID = config.getID();
    String teacherID = "";
    String remarks = txtComments.getText().trim();

    if (selectedTeacher.equals("Select a Teacher")) {
        JOptionPane.showMessageDialog(null, "Please select a teacher before submitting.");
        return;
    }

    // 1. KUHAON ANG TEACHER ID
    try (Connection conn = DriverManager.getConnection("jdbc:sqlite:aes.db?busy_timeout=5000")) {
        String findTeacher = "SELECT u_id FROM tbl_user WHERE u_name = ? AND u_type = 'Teacher'";            
        try (PreparedStatement pst = conn.prepareStatement(findTeacher)) {
            pst.setString(1, selectedTeacher);
            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    teacherID = rs.getString("u_id");
                } else {
                    JOptionPane.showMessageDialog(null, "Teacher not found.");
                    return; 
                }
            }
        }
    } catch (SQLException e) {
        System.out.println("Teacher ID Fetch Error: " + e.getMessage());
    }

    if (remarks.isEmpty()) remarks = "No specific comments provided.";

    // 2. COMPUTE AVERAGE
    double total = 0.00;
    for (int s : scores) total += s;
    double rawAverage = (scores.isEmpty()) ? 0 : total / scores.size();
    double average = Math.round(rawAverage * 100.0) / 100.0;

    // 3. I-SAVE SA DATABASE
    String sql = "INSERT INTO tbl_evaluation (t_u_id, s_u_id, e_remarks, e_average, e_year, e_sem, e_date) " +
                 "VALUES (?, ?, ?, ?, ?, ?, DATE('now'))";

    config conf = new config();
    // KINI NGA LINE MAOY MO-SAVE SA MAIN TABLE
    int evalMainId = conf.insertAndGetId(sql, teacherID, studentID, remarks, average, "2025-2026", "2nd Semester");

    if (evalMainId > 0) {
        isSubmitted = true; // I-lock na ang form

        // 4. I-SAVE ANG MATAG SCORE SA DETAILS TABLE
        for (int i = 0; i < scores.size(); i++) {
            String sqlDetails = "INSERT INTO tbl_evaluation_details (e_id, q_id, score) VALUES (?, ?, ?)";
            conf.updateRecord(sqlDetails, evalMainId, (i + 1), scores.get(i));
        }

        conf.logEvent("Evaluated a Teacher: " + selectedTeacher);
        JOptionPane.showMessageDialog(null, "Evaluation Saved! \nAverage Score: " + average);
        
        scores.clear();
        new studDashboard().setVisible(true);
        this.dispose();
    }
}
   

    
 
    private void loadQuestions() {
        String sql = "SELECT q_text FROM tbl_question ORDER BY q_id ASC";

        // Try-with-resources: Everything inside the () closes automatically
        try (Connection conn = DriverManager.getConnection("jdbc:sqlite:aes.db");
             PreparedStatement pstmt = conn.prepareStatement(sql);
             ResultSet rs = pstmt.executeQuery()) {

            questionsList.clear();
            while (rs.next()) {
                questionsList.add(rs.getString("q_text"));
            }

            if (!questionsList.isEmpty()) {
                lblQuestion.setText(questionsList.get(0));
            }
            jScrollPane1.setVisible(false);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage());
        }
    }
    public void loadTeachers() {
    try {
        config conf = new config();
        String query = "SELECT u_name FROM tbl_user " +
                       "WHERE u_type = 'Teacher' " +
                       "AND u_id NOT IN (SELECT t_u_id FROM tbl_evaluation WHERE s_u_id = '" + config.getID() + "')";
        
        ResultSet rs = conf.getData(query);
        teacherDropdown.removeAllItems();
        teacherDropdown.addItem("Select a Teacher");
        
        while (rs.next()) {
            teacherDropdown.addItem(rs.getString("u_name"));
        }
    } catch (SQLException e) {
        System.out.println("Error loading teachers: " + e.getMessage());
    }
}
    
      
